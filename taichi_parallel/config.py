"""
config.py — 全局参数配置
═══════════════════════════════════════════════════════════════

所有可调参数集中于此，修改本文件即可调整模拟行为，无需改动其他模块。

参数分组:
    · 计算域          — 模拟空间尺寸
    · SPH 离散化      — 粒子半径、支撑域、初始体积
    · 物态方程 (EOS)  — Tait 方程刚度 / 指数
    · 粘性            — 人工粘性系数
    · 时间步进 (CFL)  — 自适应步长范围、CFL 系数
    · 流体块          — 初始水体范围
    · 刚体            — 尺寸、密度、质量参数  ← 直接改 BODY_W/BODY_H 即可调整大小
    · 多刚体          — 最大刚体数
    · 碰撞            — 摩擦 / 恢复系数、SI 迭代
    · 网格            — 邻居搜索网格
    · 墙壁            — 边界粒子层数
"""

import math

# ── 计算域 ────────────────────────────────────────────────
DOMAIN_X = 2.0                          # 域宽 (m)
DOMAIN_Y = 2.0                          # 域高 (m)

# ── SPH 离散化 ────────────────────────────────────────────
# 粒子半径越小 → 粒子越多 → 画面越平滑
#   0.012  ≈   1 000 粒子  (原始)
#   0.008  ≈   3 500 粒子
#   0.005  ≈  20 000 粒子  ← 默认 (16 GB 显存流畅)
#   0.004  ≈  32 000 粒子
#   0.003  ≈  57 000 粒子  (高质量, 仍在 16 GB 范围内)
PARTICLE_RADIUS   = 0.005               # 粒子半径 r
PARTICLE_DIAMETER = 2.0 * PARTICLE_RADIUS
SUPPORT_RADIUS    = 4.0 * PARTICLE_RADIUS  # 核函数支撑半径 h = 4r
M_V0              = 0.8 * PARTICLE_DIAMETER ** 2  # 流体粒子初始体积

# ── 物态方程 (Tait EOS) ──────────────────────────────────
DENSITY_0  = 1000.0                     # 静止密度 ρ₀ (kg/m³)
STIFFNESS  = 50000.0                    # 刚度系数 B
EXPONENT   = 7.0                        # Tait 指数 γ

# ── 粘性 ──────────────────────────────────────────────────
# 越小越像水 (纯水约 0.001); 过小会不稳定
#   0.05  → 糖浆感 (原始)
#   0.02  → 稠水
#   0.015 → 接近真实水 ← 默认
#   0.008 → 快速流动感
VISCOSITY = 0.015                       # 人工粘性系数

# ── 时间步进 (CFL 自适应) ─────────────────────────────────
SOUND_SPEED = float(math.sqrt(STIFFNESS * EXPONENT / DENSITY_0))
# h 减小时 CFL 上限也应减小: dt_max ≈ CFL * h / c
DT_MAX     = 2e-4                       # 安全上限 (对应 h=0.020)
DT_MIN     = 1e-6                       # 最小时间步长 (s)
CFL_FACTOR = 0.4                        # CFL 安全系数
SUBSTEPS   = 8                          # 每帧子步数 (更多子步 → 模拟更快)

# ── 初始流体块范围 ────────────────────────────────────────
# 修改这两个值即可改变初始水体大小
FLUID_X_MAX = 1.4                       # 流体右边界 (m), 最大 < DOMAIN_X
FLUID_Y_MAX = 1.5                       # 流体上边界 (m), 最大 < DOMAIN_Y

# ── 刚体 (所有方块共享相同几何) ───────────────────────────
# ★ 直接修改 BODY_W / BODY_H 即可改变方块大小
#   示例:  0.4 × 0.12  → 宽扁长板
#          0.15 × 0.15 → 小正方体
#          0.50 × 0.30 → 大方块
BODY_W       = 0.30                     # 宽 (m)
BODY_H       = 0.18                     # 高 (m)
BODY_DENSITY = 500.0                    # 密度 (kg/m³), < ρ₀ → 会浮起

BODY_MASS     = float(BODY_DENSITY * BODY_W * BODY_H)
BODY_INV_MASS = 1.0 / BODY_MASS
BODY_I        = BODY_MASS * (BODY_W ** 2 + BODY_H ** 2) / 12.0  # 转动惯量
BODY_INV_I    = 1.0 / BODY_I

# ── 多刚体 ────────────────────────────────────────────────
MAX_BODIES = 10                         # 场景最多支持的刚体数量

# ── 网格 (用于邻居搜索) ──────────────────────────────────
GRID_SIZE   = SUPPORT_RADIUS
GRID_NX     = int(math.ceil(DOMAIN_X / GRID_SIZE))
GRID_NY     = int(math.ceil(DOMAIN_Y / GRID_SIZE))
TOTAL_GRIDS = GRID_NX * GRID_NY

# ── 墙壁 ─────────────────────────────────────────────────
WALL_LAYERS = 2                         # 边界粒子层数 (底 / 左 / 右)

# ── 碰撞 ─────────────────────────────────────────────────
FRICTION    = 0.3                       # Coulomb 摩擦系数
RESTITUTION = 0.2                       # 恢复系数
SI_ITERS    = 10                        # Sequential Impulse 迭代次数

# ── 初始场景 ──────────────────────────────────────────────
BODY0_X = 1.3                           # 第 0 号刚体初始 x
BODY0_Y = 0.3                           # 第 0 号刚体初始 y
